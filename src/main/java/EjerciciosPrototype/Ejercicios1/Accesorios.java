package EjerciciosPrototype.Ejercicios1;

public class Accesorios {
    private Boolean tieneAudifono;
    private Boolean tieneCargador;
    private Boolean tieneEstuche;

    public Accesorios() {
    }

    public Boolean getTieneAudifono() {
        return tieneAudifono;
    }

    public void setTieneAudifono(Boolean tieneAudifono) {
        this.tieneAudifono = tieneAudifono;
    }

    public Boolean getTieneCargador() {
        return tieneCargador;
    }

    public void setTieneCargador(Boolean tieneCargador) {
        this.tieneCargador = tieneCargador;
    }

    public Boolean getTieneEstuche() {
        return tieneEstuche;
    }

    public void setTieneEstuche(Boolean tieneEstuche) {
        this.tieneEstuche = tieneEstuche;
    }
}
